package observer;

public class TrainerEvent {
	String topic;

	String timepass;

	public String getTopic() {
		return topic;
	}

	public void setTopic(String topic) {
		this.topic = topic;
	}

	public String getTimepass() {
		return timepass;
	}

	public void setTimepass(String timepass) {
		this.timepass = timepass;
	}

}
